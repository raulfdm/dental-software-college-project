    private void salvarPaciente(int id) {

        for (int c = 0; c < listaCadastro.size(); c++) {
            if (listaCadastro.get(c).id == id) {
                listaCadastro.remove(c);
            }
        }
        for (int p = 0; p < listaPaciente.size(); p++) {
            if (listaPaciente.get(p).idPaciente == id) {
                listaPaciente.remove(p);
            }
        }
        for (int a = 0; a < listaAnamnese.size(); a++) {
            if (listaAnamnese.get(a).idAnamnese == id) {
                listaAnamnese.remove(a);
            }
        }

        salvarPacienteImpl();
        salvarAnamneseImpl();

    }

