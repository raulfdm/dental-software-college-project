    private void salvarPacienteImpl() {
        try {
            //Classe Cadastro
            cadastro.setId(Integer.parseInt(txtIdCadastro.getText()));
            cadastro.setCpf(txtCpf.getText());
            cadastro.setNomeCompleto(txtNomeCompleto.getText());

            //Classe Paciente
            cadastro.paciente.setIdPaciente(Integer.parseInt(txtIdCadastro.getText()));
            cadastro.paciente.setNomeCompleto(txtNomeCompleto.getText());
            cadastro.paciente.setDataCadastro(txtDataCadastro.getText());
            String mes = (String) cboMesNasc.getSelectedItem();
            String mesNasc = getMes(mes);
            String diaNasc = (String) cboDiaNasc.getSelectedItem();
            String anoNasc = (String) cboAnoNasc.getSelectedItem();
            cadastro.paciente.setDiaNasc(diaNasc);
            cadastro.paciente.setMesNasc(mes);
            cadastro.paciente.setAnoNasc(anoNasc);
            cadastro.paciente.setDataNascimento(getDataNasc(diaNasc, mesNasc, anoNasc));
            cadastro.paciente.setIdade(calcIdade(mesNasc, anoNasc));
            cadastro.paciente.setNomePai(txtNomePai.getText());
            cadastro.paciente.setNomeMae(txtNomeMae.getText());
            cadastro.paciente.setSexo((String) cboSexo.getSelectedItem());
            cadastro.paciente.setEstadoCivil((String) cboEstadoCivil.getSelectedItem());
            cadastro.paciente.setRg(txtRg.getText());
            cadastro.paciente.setCpf(txtCpf.getText());
            cadastro.paciente.setProfissao(txtProfissao.getText());
            cadastro.paciente.setEmpresa(txtEmpresa.getText());
            cadastro.paciente.setEndereco(txtEndereco.getText());
            cadastro.paciente.setNumeroCasa(Integer.parseInt(txtNumero.getText()));
            cadastro.paciente.setCep(txtCep.getText());
            cadastro.paciente.setComplemento(txtComplemento.getText());
            cadastro.paciente.setBairro(txtBairro.getText());
            cadastro.paciente.setCidade(txtCidade.getText());
            cadastro.paciente.setEstado((String) cboEstado.getSelectedItem());
            cadastro.paciente.setTelCelular(trataTelefone(txtTelCelular.getText()));
            cadastro.paciente.setTelResidencial(trataTelefone(txtTelResidencial.getText()));
            cadastro.paciente.setTelRecado(trataTelefone(txtTelRecado.getText()));
            cadastro.paciente.setComentario(txtComentario.getText());

            listaCadastro.add(cadastro);
            listaPaciente.add(cadastro.paciente);
            JOptionPane.showMessageDialog(null, "Paciente cadastrado com sucesso.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar as informações pessoais.");
        }
    }

