    public String dataHoje() {
        Calendar hoje = Calendar.getInstance();
        int dia = hoje.get(Calendar.DAY_OF_MONTH);
        int mes = hoje.get(Calendar.MONTH) + 1;
        int ano = hoje.get(Calendar.YEAR);

        String dataFull = dia + "/" + mes + "/" + ano;
        return dataFull;
    }

    public String calcIdade(String mesNasc, String anoNasc) {
        int age;
        String idadeFinal;
        String formatoData = "MM/yyyy";
        DateFormat df = new SimpleDateFormat(formatoData);
        Date dataNascInput = null;
        try {

            dataNascInput = df.parse(mesNasc + "/" + anoNasc);

        } catch (Exception e) {
            System.out.println("ERRO DATA");
        }

        Calendar dateOfBirth = new GregorianCalendar();
        dateOfBirth.setTime(dataNascInput);

        Calendar hoje = Calendar.getInstance();

        age = hoje.get(Calendar.YEAR) - dateOfBirth.get(Calendar.YEAR);

        if (hoje.get(Calendar.MONTH) < dateOfBirth.get(Calendar.MONTH)) {
            age--;

        }
        idadeFinal = String.valueOf(age);

        txtIdade.setText(idadeFinal);
        return idadeFinal;
    }

