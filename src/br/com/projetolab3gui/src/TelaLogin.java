    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed

        //Pega os valores do campo
        String txt_Senha = txtSenha.getText();
        String txt_Usuario = txtUsuario.getText();

        //Checa se o valor digitado é o mesmo cadastrado
        if (txt_Usuario.equals(usuarioP) || txt_Senha.equals(senhaP)) {
            TelaHome h = new TelaHome();
            h.setVisible(true);
            this.setVisible(false);
            
        } else {
            String mensagem = "Senha incorreta, digite novamente";
            JOptionPane.showMessageDialog(null, mensagem);
            limparDados();
        }
    }//GEN-LAST:event_btnEntrarActionPerformed

